numero = 14

if numero == 10: #True
    print("Este número é igual a 10")
elif numero == 11:
    print("Este número é igual a 11!")
elif numero == 12:
    print("Este número é igual a 12!")
elif numero == 13:
    print("Este número é igual a 13!")    
else: #False
    print("Este número não é igual nem a 10, 11, 12 ou 13!")
